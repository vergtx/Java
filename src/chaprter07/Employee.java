package chaprter07;

public class Employee {
	
	private String name;
	private int age;
	private String dept;
	
	public String getName() {
		return name;
	}
	
	public void setName() {
		this.name = name;
	}
	
	public int getAge( ) {
		return age;
	}
	
	public void setAge(int age) {
		this.age = age ;
		
	}
	
	public String getDept() {
		return dept;
		
	}
	
	public void setDept () {
		this.dept = dept ;
	}
	
}
